package com.dto;

public class Nurse {
	
	private String NurseName;
	private int NurseAge;
	private int TelePhone;
	private String NurseWard;
	
	private int NurseID;
	public int getNurseID() {
		return NurseID;
	}
	public void setNurseID(int nurseID) {
		NurseID = nurseID;
	}
	public String getNurseName() {
		return NurseName;
	}
	public void setNurseName(String nurseName) {
		NurseName = nurseName;
	}
	public int getNurseAge() {
		return NurseAge;
	}
	public void setNurseAge(int nurseAge) {
		NurseAge = nurseAge;
	}
	public int getTelePhone() {
		return TelePhone;
	}
	public void setTelePhone(int telePhone) {
		TelePhone = telePhone;
	}
	public String getNurseWard() {
		return NurseWard;
	}
	public void setNurseWard(String nurseWard) {
		NurseWard = nurseWard;
	}

	
	
	
	
}
